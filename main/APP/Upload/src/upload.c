#include "./Upload/inc/upload.h"
#include "main.h"

upload_param_t sg_uploadparam_t =
{
    .ip = {47, 104, 250, 225},
    .port = 18080U,
};

/*
*********************************************************************************************************
*    函 数 名: upload_set_upload_addr
*    功能说明: 从系统配置(struct upload_addr)同步上传 IP、端口到上传模块参数
*    形    参: 无
*    返 回 值: 无。
*********************************************************************************************************
*/
void upload_set_upload_addr(void)
{
    struct upload_addr *cfg = (struct upload_addr *)app_get_http_upload_function();
    if (cfg == NULL) {
        return;
    }
    memcpy(sg_uploadparam_t.ip, cfg->ip, 4);
    sg_uploadparam_t.port = cfg->port;
}

/*
*********************************************************************************************************
*    函 数 名: upload_set_upload_mode
*    功能说明: 设置当前上传模式(有线/无线/空闲)。
*    形    参: mode 上传模式
*    返 回 值: 无。
*********************************************************************************************************
*/
void upload_set_upload_mode(uint8_t mode)
{
    sg_uploadparam_t.mode = mode;
}

/*
*********************************************************************************************************
*    函 数 名: upload_get_mode_function
*    功能说明: 获取当前上传模式。
*    形    参: 无
*    返 回 值: 上传模式。
*********************************************************************************************************
*/
uint8_t upload_get_mode_function(void)
{
    return sg_uploadparam_t.mode;
}

/*
*********************************************************************************************************
*    函 数 名: upload_get_infor_function
*    功能说明: 获取上传参数信息。
*    形    参: 无
*    返 回 值: 上传参数信息指针。
*********************************************************************************************************
*/
void *upload_get_infor_function(void)
{
    

    return &sg_uploadparam_t;
}

